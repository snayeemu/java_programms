public class Calculator {
    private double firstNumber;
    private double secondNumber;

    public void setFirstNumber(double firstNumber) {
        this.firstNumber = firstNumber;
    }

    public void setSecondNumber(double secondNumber) {
        this.secondNumber = secondNumber;
    }
    public double summation(){ return firstNumber + secondNumber; }
    public double subtraction (){ return firstNumber - secondNumber; }
    public double multiplication(){ return firstNumber * secondNumber; }
    public double deviation (){ return firstNumber / secondNumber; }
}

