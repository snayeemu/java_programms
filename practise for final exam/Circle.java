public class Circle
{
	private double radius;
	
//	static final double pi = 3.1416;
	
	public Circle (double x)
	{
		radius=x;
		
	}
			
	public void setRadius(double m)
	{
		radius=m;
	}
	
	public double displayArea()
	{
		return Math.PI*radius*radius;
	}
	
}


